// What will be the output of the following code snippet?

public class SwitchDecimal {
    public static void main(String[] args) {
        int num = 2.2;
        switch(num) {
            case 1.2:
                System.out.print("one");
            case 2.2:
                System.out.print("two");
            case 3.2:
                System.out.println("three"); 
        }
    }
}